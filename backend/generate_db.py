def generate(bases_list, mods):
    for b_name, b_c, b_p, b_cb, b_f in bases_list:
        for m_name, m_c, m_p, m_cb, m_f in mods:
            name = b_name + m_name
            c = round(b_c * m_c)
            p = round(b_p * m_p, 1)
            cb = round(b_cb * m_cb, 1) if b_cb > 0 else round(m_cb, 1)
            f = round(b_f * m_f, 1)
            db.append({
                "food_name": name,
                "calories": c,
                "protein": p,
                "carbs": cb,
                "fat": f
            })

